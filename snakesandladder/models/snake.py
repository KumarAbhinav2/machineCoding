class Snake:

    def __init__(self):
        self.id = None
        self.head = None
        self.tail = None

    def setId(self, id):
        self.id = id

    def getId(self):
        return self.id

    def getHead(self):
        return self.head

    def setHead(self, head):
        self.head = head

    def getTail(self):
        return self.tail

    def setTail(self, tail):
        self.tail = tail

