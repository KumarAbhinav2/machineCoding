class Ladder:

    def __init__(self):
        self.id = None
        self.start = None
        self.end = None

    def setId(self, id):
        self.id = id

    def getId(self):
        return self.id

    def getStart(self):
        return self.start

    def setStart(self, start):
        self.start = start

    def getEnd(self):
        return self.end

    def setEnd(self, end):
        self.end = end
